package polynomial;

/**
 * Common constants used in the polynomial package.
 */
public class PolynomialConstants {
  /**
   * A string representation of an empty polynomial.
   */
  public static final String EMPTY_POLYNOMIAL = "0";
  /**
   * A string representation of the plus symbol.
   */
  public static final String PLUS_SYMBOL = "+";
  /**
   * A string representation of the x^ symbol.
   */
  public static final String X_POWER_SYMBOL = "x^";

  /**
   * A blank or empty an empty string.
   */
  public static final String EMPTY_STRING = "";

  /**
   * A private constructor to prevent instantiation of this class since all fields are
   * static.
   */
  private PolynomialConstants() {
  }
}
